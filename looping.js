/**
 * Looping menggunakan for.
 * Menampilkan angka 1 - 10.
 */
for (let i = 1; 1 < 11; i++) {
    console.log(`Perulangan ke: ${i}`);
}

// membuat variable awal
let i = 1;

/**
 * Looping menggunakan while.
 * Membuat kondisi untuk batasan looping.
 */
while (i < 11) {
    console.log(`Perulangan ke: ${i}`);
    // melakukan increment
    i++;
}